"""
entities.py — извлекает геологические сущности из текста отчёта.

Track 1 требует находить: минералы, местонахождения, химические составы.
Для хакатона используем словарный подход (списки ключевых слов) + regex
для содержаний металлов. Это просто, быстро и объяснимо судьям.

Можно потом улучшить через spaCy NER, но для рабочего прототипа
словарь покрывает большинство случаев в этих отчётах.
"""

import re

from parse_catalog import dms_to_decimal

# Минералы/металлы по ОСНОВЕ слова: русский текст склоняет их по падежам
# ("золота", "серебром", "медный"), поэтому ищем стем + любое окончание (\w*),
# а не точное вхождение. Ключ — каноническое имя для вывода, значение — паттерн.
MINERAL_PATTERNS = {
    "золото":   r"золот\w*",
    "серебро":  r"серебр\w*",
    "висмут":   r"висмут\w*",
    "медь":     r"\bмед(?:ь|и|н)\w*",
    "цинк":     r"цинк\w*",
    "свинец":   r"свин(?:ец|ц)\w*",
    "молибден": r"молибден\w*",
    "вольфрам": r"вольфрам\w*",
    "кобальт":  r"кобальт\w*",
    "ртуть":    r"ртут[ьи]\w*",
    "мышьяк":   r"мышья[кч]\w*",
    "уран":     r"\bуран\w*",
    "кварц":    r"кварц\w*",
    "пирит":    r"пирит\w*",
}
_MINERAL_COMPILED = {name: re.compile(p) for name, p in MINERAL_PATTERNS.items()}

# содержание металла вида "13,2 г/т", "23,9 г-т", "10 г/т".
# Требуем именно дробную черту (/ или -), а не точку: иначе "1973 г. тонн"
# (где "г." = год) ложно матчилось бы как содержание. \b отсекает "г/тонну".
GRADE = re.compile(r"(\d+(?:[,.]\d+)?)\s*г\s*[/\-]\s*т\b")

# координаты в тексте: "48°03' с.ш.", "71°16' в.д.", "105°20' в.д."
# Градусы 1–3 цифры: широта ≤90 (2 цифры), но долгота может быть 3-значной
# (>100° в.д.) в восточных регионах — слишком большое значение отсекается ниже
# проверкой диапазона (valid=false), а не молчаливым пропуском строки.
COORD_IN_TEXT = re.compile(
    r"(\d{1,3})\s*[°o]\s*(\d{2})['′]?\s*(с\.?ш\.?|в\.?д\.?)",
    re.IGNORECASE
)


def _looks_like_year(token):
    """True для 4-значного числа в диапазоне календарного года (1900–2099)."""
    return token.isdigit() and len(token) == 4 and 1900 <= int(token) <= 2099


def extract_entities(text):
    """Возвращает словарь найденных сущностей."""
    low = text.lower()

    found_minerals = sorted(
        name for name, rx in _MINERAL_COMPILED.items() if rx.search(low)
    )

    # Отсекаем значения, выглядящие как календарный год (целое 1900–2099):
    # "1973 г-т" — это год, а не содержание. Реальные содержания обычно с
    # запятой ("1,6") или небольшие, поэтому фильтр их не трогает.
    grades = [g for g in GRADE.findall(text) if not _looks_like_year(g)]

    # Координаты, упомянутые в прозе. Как и в каталоге, не теряем молча:
    # проверяем диапазон, считаем десятичные градусы, неправдоподобные
    # помечаем "valid": false вместо тихого отбрасывания.
    coords = []
    for d, mins, kind in COORD_IN_TEXT.findall(text):
        deg, minute = int(d), int(mins)
        # широта (с.ш.) <= 90°, долгота (в.д.) <= 180° — как в каталоге
        max_deg = 90 if kind.lower().startswith("с") else 180
        valid = 0 <= deg <= max_deg and 0 <= minute < 60
        entry = {
            "value": f"{deg}°{minute:02d}'",
            "type": kind,
            "valid": valid,
        }
        if valid:
            entry["decimal"] = dms_to_decimal(deg, minute)
        coords.append(entry)

    return {
        "minerals": found_minerals,
        "grades_g_per_t": grades,        # содержания металлов
        "coordinates_in_text": coords,   # координаты, упомянутые в прозе
    }


# номер рядом с контекстным словом: "отчёт № 25834", "инв. 25834", "N 25834"
_REPORT_CONTEXT = re.compile(
    r"(?:отч[её]т|инв(?:\.|ентарн\w*)?|№|\bN\b)\D{0,6}(\d{4,6})",
    re.IGNORECASE,
)


def _pick_report_number(candidates):
    """
    Из списка 4–6-значных чисел выбирает наиболее правдоподобный номер отчёта:
    предпочитает не-годы, среди них — самое длинное (номера обычно 5–6 цифр).
    Возвращает строку или None.
    """
    if not candidates:
        return None
    non_years = [c for c in candidates if not _looks_like_year(c)]
    pool = non_years or candidates  # если остались одни «годы» — берём из них
    return max(pool, key=len)


def extract_report_id(filename, text):
    """
    Пытается найти номер отчёта (например 25834). Длина 4–6 цифр — не
    привязываемся к формату одного датасета и не путаем номер с годом.

    Порядок: (1) число рядом со словом-контекстом в тексте ("отчёт № …");
    (2) самое правдоподобное 4–6-значное число в имени файла (не год);
    (3) фоллбэк — любое 4–6-значное число в тексте, чтобы ничего не терять.
    """
    m = _REPORT_CONTEXT.search(text)
    if m:
        return m.group(1)

    picked = _pick_report_number(re.findall(r"\d{4,6}", filename))
    if picked:
        return picked

    m = re.search(r"\b(\d{4,6})\b", text)
    return m.group(1) if m else None
