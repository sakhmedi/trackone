"""
parse_catalog.py — вытаскивает строки каталога координат из OCR-текста.

Формат строк в каталоге (см. каталог_координат_25834):
    1.  Ю.Жуманай      48 03      71 16
    2   Жабай          48 07      71 20
    ...

То есть: [номер] [название участка] [широта: градусы минуты] [долгота: градусы минуты]

Подход — регулярные выражения. Не нейросеть: для табличных данных
regex точнее, быстрее и полностью воспроизводим, что и ценят критерии.
"""

import re


# координата вида "48 03", "48°03'", "47 58" — два числа (градусы и минуты)
# OCR может вставить градус/штрих/мусор между ними, поэтому допускаем разное
COORD = r"(\d{2})\s*[°o\s]\s*(\d{2})"

# строка каталога: номер в начале, потом название, потом две координаты
LINE = re.compile(
    r"^\s*(\d{1,2})[.\s]+"        # номер участка
    r"(.+?)\s+"                   # название (нежадно, до координат)
    r"" + COORD + r"\D+?"         # широта
    + COORD,                     # долгота
    re.MULTILINE
)


def dms_to_decimal(degrees, minutes):
    """Переводит градусы+минуты в десятичные градусы. 48 03 -> 48.05"""
    return round(int(degrees) + int(minutes) / 60.0, 6)


def parse_catalog(text):
    """
    Находит все строки каталога координат в тексте.
    Возвращает список словарей с названием и координатами.
    """
    results = []
    for m in LINE.finditer(text):
        num, name, lat_d, lat_m, lon_d, lon_m = m.groups()
        results.append({
            "id": num.strip(),
            "name": name.strip(),
            "lat_dms": f"{lat_d}°{lat_m}'",
            "lon_dms": f"{lon_d}°{lon_m}'",
            # десятичные координаты пригодятся для Track 3 (геопривязка)
            "lat_decimal": dms_to_decimal(lat_d, lat_m),
            "lon_decimal": dms_to_decimal(lon_d, lon_m),
        })
    return results
