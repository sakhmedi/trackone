"""
parse_catalog.py — вытаскивает строки каталога координат из OCR-текста.

Формат строк в каталоге (см. каталог_координат_25834):
    1.  Ю.Жуманай      48 03      71 16
    2   Жабай          48 07      71 20
    ...
То есть: [номер] [название участка] [широта: градусы минуты] [долгота: градусы минуты]

Подход — регулярные выражения. Не нейросеть: для табличных данных regex
точнее, быстрее и полностью воспроизводим, что и ценят критерии.

Устойчивость к OCR: цифры в координатах часто распознаются как похожие буквы
(0↔О/O, 1↔l/I, 3↔З, 8↔В, 5↔S/s, 6↔b, 2↔Z). Мы допускаем такие символы в
позициях координат и нормализуем их в цифры. Каждая строка проверяется на
допустимый диапазон; нечитаемые/неправдоподобные строки НЕ выбрасываются молча,
а помечаются "valid": false — чтобы данные не терялись незаметно.
"""

import re


# Документ считаем каталогом координат, только если в нём набирается хотя бы
# столько ВАЛИДНЫХ строк. Это отсекает ложные срабатывания на сплошном тексте и
# таблицах расходов, где случайные числа изредка складываются в «координату».
# Порог генеричный (не привязан к региону/файлу): настоящий каталог плотный.
MIN_CATALOG_ROWS = 4


# карта «похоже на цифру» -> цифра (типичные ошибки кириллического OCR)
_DIGIT_MAP = str.maketrans({
    "O": "0", "o": "0", "О": "0", "о": "0",
    "l": "1", "I": "1", "|": "1", "і": "1",
    "З": "3", "з": "3",
    "В": "8",
    "S": "5", "s": "5",
    "b": "6",
    "Z": "2",
})

# символы, которые в позиции координаты считаем «цифрой» (цифра или её OCR-двойник)
_DIGITLIKE = r"[0-9OoОоlI|іЗзВSsbZ]"
# разделитель между градусами и минутами: знак градуса/штрих/пробел/мусор (или ничего)
_SEP = r"[°ºˈ'’\s]*"
# одна координата: 2–3 «цифры» (градусы) + разделитель + 2 «цифры» (минуты)
_COORD = rf"({_DIGITLIKE}{{2,3}}){_SEP}({_DIGITLIKE}{{2}})"

# строка каталога: [мусор OCR] номер -> название -> широта -> долгота
# OCR часто ставит хлам в начале строки ("; 28 ...", "| 48 ...", "д 29 ..."),
# поэтому допускаем до 3 не-цифровых символов перед номером участка.
LINE = re.compile(
    r"^\D{0,3}?(\d{1,2})[.\)\s\-—]+"  # [мусор] номер участка
    r"(.+?)\s+"                       # название (нежадно, до координат)
    + _COORD + r"\D+?"                # широта + разделитель до долготы
    + _COORD,                         # долгота
    re.MULTILINE,
)


def _norm_int(token):
    """'4О' -> 40. Возвращает int или None, если ничего цифрового не осталось."""
    cleaned = re.sub(r"\D", "", token.translate(_DIGIT_MAP))
    return int(cleaned) if cleaned else None


_CYR = re.compile(r"[А-Яа-яЁё]")


def _clean_name(raw):
    """
    Оставляет от названия осмысленную «головную» часть. Внутри названия
    допускаем цифры — OCR нередко вставляет их в букву ('Жум4най'), и обрезать
    имя на первой же цифре нельзя. Поэтому берём буквы/цифры/пробелы/точки/дефисы,
    а затем срезаем только ХВОСТОВОЙ мусор (цифры/пробелы/точки), который мог
    приклеиться от соседней колонки координат.
    """
    m = re.match(r"[А-Яа-яЁё][А-Яа-яЁё0-9 .\-]*", raw.strip())
    if not m:
        return ""
    return re.sub(r"[\s\d.\-]+$", "", m.group(0)).strip()


def dms_to_decimal(degrees, minutes):
    """Переводит градусы+минуты в десятичные градусы. 48, 3 -> 48.05"""
    return round(degrees + minutes / 60.0, 6)


def parse_catalog(text):
    """
    Находит все строки каталога координат в тексте.
    Возвращает список словарей. Невалидные строки помечаются "valid": false.
    """
    results = []
    for m in LINE.finditer(text):
        num, name, lat_d, lat_m, lon_d, lon_m = m.groups()

        # Фильтр шума: настоящая строка каталога имеет осмысленное название.
        # Если в названии меньше 3 кириллических букв — это, скорее всего,
        # ложное срабатывание на OCR-каше, а не участок. Пропускаем.
        name = _clean_name(name)
        if len(_CYR.findall(name)) < 3:
            continue

        latd, latm = _norm_int(lat_d), _norm_int(lat_m)
        lond, lonm = _norm_int(lon_d), _norm_int(lon_m)

        valid = (
            None not in (latd, latm, lond, lonm)
            and 0 <= latd <= 90 and 0 <= latm < 60
            and 0 <= lond <= 180 and 0 <= lonm < 60
        )

        entry = {
            "id": num.strip(),
            "name": name.strip(),
            "valid": valid,
        }
        if valid:
            entry["lat_dms"] = f"{latd}°{latm:02d}'"
            entry["lon_dms"] = f"{lond}°{lonm:02d}'"
            entry["lat_decimal"] = dms_to_decimal(latd, latm)
            entry["lon_decimal"] = dms_to_decimal(lond, lonm)
        else:
            # сохраняем как распозналось — для ручной проверки, ничего не теряем
            entry["lat_dms_raw"] = f"{lat_d} {lat_m}"
            entry["lon_dms_raw"] = f"{lon_d} {lon_m}"
            entry["warning"] = "координаты нечитаемы или вне допустимого диапазона"
        results.append(entry)

    # Порог плотности: если валидных строк мало — это не каталог, а шум прозы.
    if sum(1 for r in results if r["valid"]) < MIN_CATALOG_ROWS:
        return []
    return results
